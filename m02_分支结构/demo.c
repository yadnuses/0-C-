/* ============================================================
 * M2 分支结构 —— 配套演示程序（demo）
 * ------------------------------------------------------------
 * 这个程序把"讲义"里讲到的每个核心考点，都用一段可以真正
 * 跑起来的代码演示一遍。建议你：
 *   1) 先读讲义对应小节，再回来看这里对应的演示；
 *   2) 编译运行，亲眼看到输出，再回头对照注释里说的"为什么"。
 *
 * 编译运行（在本文件所在目录打开终端）：
 *   gcc demo.c -o demo        // math.h 通常不用额外加 -lm，若报错就用下面这行
 *   gcc demo.c -o demo -lm    // -lm 表示链接数学库（fabs 在 math.h 里）
 *   ./demo
 *
 * 为了让你一次运行就看到所有考点，下面大多用"固定值"演示，
 * 不需要你反复输入。真正练手输入请看同目录的 练习.c。
 * ============================================================ */

#include <stdio.h>
#include <math.h>   /* 第 8 段浮点判等要用 fabs()，所以这里要包含 math.h */

int main(void)
{
    /* ========================================================
     * 演示 1：if-else 双分支 —— 判断奇偶（对应讲义 二）
     * --------------------------------------------------------
     * 规则："二者必执其一"。条件真走 if 块，条件假走 else 块，
     * 绝不会两块都走，也绝不会两块都不走。
     * 条件 n % 2 == 0：n 除以 2 的余数是不是 0。余 0 是偶数。
     * ⚠️ 这里是 ==（两个等号，判等），不是 =（一个等号，赋值）。
     * ======================================================== */
    int n = 7;
    printf("【演示1】判断 %d 的奇偶：\n", n);
    if (n % 2 == 0) {
        printf("    %d 是偶数\n", n);
    } else {
        printf("    %d 是奇数\n", n);   /* 7 余 1，走这里 */
    }

    /* ========================================================
     * 演示 2：全场最大的坑 —— = 和 == 的区别（对应讲义 五）
     * --------------------------------------------------------
     * if (x == 5)：判断 x 是不是 5，结果真/假，x 的值不变。
     * if (x = 5) ：先把 5 装进 x（偷偷改了 x！），整个表达式
     *              的值就是刚装进去的 5，5 是非 0 → 恒为真。
     * 下面故意演示这个 bug：明明 x 初值是 3，却照样进了 if。
     * ======================================================== */
    int x = 3;
    printf("【演示2】= 和 == 的区别（x 初值 = %d）：\n", x);
    if (x == 5) {                       /* 正确写法：3 != 5，条件为假，不打印 */
        printf("    [==] x 等于 5\n");
    } else {
        printf("    [==] x 不等于 5，且 x 仍然是 %d（没被改）\n", x);
    }
    if (x = 5) {                        /* 错误写法：赋值后 x 变 5，表达式值 5 → 恒真 */
        printf("    [= ] 居然进来了！因为 x=5 的值是 5（非0为真），x 已被偷改成 %d\n", x);
    }

    /* ========================================================
     * 演示 3：C 语言的"真"和"假"（对应讲义 六）
     * --------------------------------------------------------
     * 核心设定：0 是假，任何非 0 的数都是真（不是只有 1 才真）。
     * 反过来，关系运算 (5 > 3) 算出的结果：真就是 1，假就是 0。
     * ======================================================== */
    printf("【演示3】真和假：\n");
    if (-7) {                           /* -7 是非 0，为真 */
        printf("    if(-7) 成立，因为非 0 即为真\n");
    }
    if (0) {                            /* 0 为假，整段跳过 */
        printf("    这句永远不会打印\n");
    }
    printf("    (5 > 3) 算出来是 %d，(5 < 3) 算出来是 %d\n", 5 > 3, 5 < 3);

    /* ========================================================
     * 演示 4：else if 多分支 —— 成绩等级（对应讲义 七）
     * --------------------------------------------------------
     * 规则：从上往下一个个试，谁先成立就执行谁，然后整个阶梯
     * 立刻结束，后面的全部跳过。
     * ⚠️ 顺序必须"由大到小"：若把 >=60 写最前面，85 分会被它
     * 先截胡，错判成 C。所以这里严格按 90→80→60 排。
     * ======================================================== */
    int score = 85;
    char grade;
    printf("【演示4】%d 分的等级：\n", score);
    if (score >= 90)      grade = 'A';
    else if (score >= 80) grade = 'B';   /* 85 在这里命中，后面不再判 */
    else if (score >= 60) grade = 'C';
    else                  grade = 'D';
    printf("    等级 = %c\n", grade);

    /* ========================================================
     * 演示 5：逻辑运算符 && || ! 与短路求值（对应讲义 八）
     * --------------------------------------------------------
     * && 两个都真才真；|| 有一个真就真；! 把真假取反。
     * ⚠️ 区间不能写成 18 <= age <= 60，C 里必须拆成两个条件用 && 连。
     * 短路：&& 左边为假，右边根本不算 —— 常用来防"除以 0"。
     * ======================================================== */
    int age = 25;
    printf("【演示5】逻辑运算与短路：\n");
    if (age >= 18 && age <= 60) {        /* 正确的区间写法：用 && 把两个条件连起来 */
        printf("    %d 岁在劳动年龄区间内（用 && 连接两个条件）\n", age);
    }

    int a = 0;
    int b = 10;
    /* a != 0 为假 → 整体注定为假 → 短路 → b / a 根本不执行，不会除 0 崩溃 */
    if (a != 0 && b / a > 1) {
        printf("    这句不会打印\n");
    } else {
        printf("    a 是 0，靠短路(a!=0 为假)跳过了 b/a，没有除 0 崩溃\n");
    }

    /* ========================================================
     * 演示 6：字符 char + switch + 贯穿（对应讲义 九、十）
     * --------------------------------------------------------
     * 字符常量用单引号 '+' ；底层是 ASCII 整数，能直接拿来比对。
     * switch 拿一个值"对号入座"。⚠️ 每个 case 后通常都要 break，
     * 漏了会"贯穿"——命中后一路往下执行直到遇到 break。
     * 这里 case 6 故意不写 break，让 6、7 共用"周末"那段代码。
     * ======================================================== */
    char op = '+';
    printf("【演示6-1】switch 判断运算符 '%c'：\n", op);
    switch (op) {                        /* switch 括号里也能放 char（本质是整数） */
        case '+': printf("    这是加法\n"); break;
        case '-': printf("    这是减法\n"); break;
        default:  printf("    未知运算符\n"); break;
    }

    int day = 6;
    printf("【演示6-2】switch 判断星期 %d（故意用贯穿合并 case）：\n", day);
    switch (day) {
        case 1:
        case 2:
        case 3:
        case 4:
        case 5:
            printf("    工作日\n");      /* 1~5 任意一个命中都会贯穿到这里 */
            break;
        case 6:                          /* 命中 6，没 break，贯穿到 case 7 的代码 */
        case 7:
            printf("    周末\n");        /* 6 和 7 共用这一段，这是"故意贯穿"的正经用法 */
            break;
        default:
            printf("    星期输入错误\n");
    }

    /* ========================================================
     * 演示 7：三目运算符 ?:（对应讲义 十一）
     * --------------------------------------------------------
     * 写法：条件 ? 真时取的值 : 假时取的值。
     * 它就是"二选一赋值"的迷你 if-else，一行搞定。
     * ======================================================== */
    int p = 5, q = 8;
    int max = (p > q) ? p : q;           /* p>q 不成立 → 取冒号后面的 q */
    printf("【演示7】%d 和 %d 的较大值 = %d（三目运算符）\n", p, q, max);

    /* ========================================================
     * 演示 8：浮点数不能用 == 判等（对应讲义 十三）
     * --------------------------------------------------------
     * 小数在电脑里是近似存储，0.1 + 0.2 实际不等于 0.3。
     * 正确做法：判断两数之差的绝对值是否小于一个极小数(1e-6)。
     * fabs() 求绝对值，来自 math.h。
     * ======================================================== */
    double d = 0.1 + 0.2;
    printf("【演示8】浮点判等（0.1 + 0.2 实际 = %.17f）：\n", d);
    if (d == 0.3) {                      /* ❌ 直接 == 会判成不相等 */
        printf("    [==]   相等\n");
    } else {
        printf("    [==]   居然不相等！这就是浮点判等的坑\n");
    }
    if (fabs(d - 0.3) < 1e-6) {          /* ✅ 差足够小就当相等 */
        printf("    [fabs] 用 fabs(差) < 1e-6 判断：相等\n");
    }

    return 0;
}
