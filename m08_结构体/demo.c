/* ============================================================
 * M8 结构体 —— 配套演示程序（demo）
 * ------------------------------------------------------------
 * 这个程序把"讲义"里讲到的每个核心考点，都用一段可以真正
 * 跑起来的代码演示一遍。建议你：
 *   1) 先读讲义对应小节，再回来看这里对应的演示；
 *   2) 编译运行，亲眼看到输出，再回头对照注释里说的"为什么"。
 *
 * 编译运行（在本文件所在目录打开终端）：
 *   gcc demo.c -o demo
 *   ./demo
 *
 * 全用"固定值"演示，不需要你反复输入。练手输入请看 练习.c。
 * ============================================================ */

#include <stdio.h>
#include <string.h>      /* 字符串成员赋值用 strcpy，需要这个头文件 */

/* ============================================================
 * 定义结构体类型 —— 对应讲义 二
 * ------------------------------------------------------------
 * 这只是"画一张空白表格的模板"，此刻还没有任何学生数据。
 * ⚠️⚠️ 大括号后面那个分号绝不能漏（改错题头号坑）。
 * ============================================================ */
struct Student {
    char  name[20];                  /* 字符串成员 */
    int   age;                       /* 整数成员 */
    float score;                     /* 小数成员 */
};                                   /* ← 这个分号必须有 */

/* ============================================================
 * 考研要求的格式 —— 对应讲义 十二点五
 * ------------------------------------------------------------
 * 王道/真题里几乎都用 typedef + ElemType 占位，认脸：
 *   ElemType 是"元素类型"的占位别名，这里就当成 int；
 *   typedef struct {...} SqList; 之后声明变量不用带 struct。
 * ============================================================ */
typedef int ElemType;                /* 元素类型占位，本题当 int */
typedef struct {
    ElemType data[100];              /* 存数据的数组 */
    int length;                      /* 当前表长 */
} SqList;                            /* SqList 就是类型名（别名） */

/* 函数声明：演示传值 vs 传指针 */
void printStu(struct Student s);             /* 传值：收一份完整复制 */
void birthday(struct Student *p);            /* 传指针：能改到外面 */

int main(void)
{
    /* ========================================================
     * 演示 1：声明、初始化、用点 . 访问成员（对应讲义 三、四）
     * --------------------------------------------------------
     * 初始化时大括号里的值按成员顺序一一对应填进去。
     * 访问某一栏用"变量名.成员名"，把 s1.age 当普通 int 用即可。
     * ⚠️ 不能 printf 一整张表，必须一个成员一个成员地打。
     * ======================================================== */
    struct Student s1 = {"张三", 20, 95.5};
    printf("【演示1】姓名:%s 年龄:%d 分数:%.1f\n",
           s1.name, s1.age, s1.score);
    s1.age = 21;                     /* 数字成员可直接用 = 改 */
    printf("    改年龄后：age=%d\n", s1.age);

    /* ========================================================
     * 演示 2：字符串成员要用 strcpy，不能用 =（对应讲义 五）
     * --------------------------------------------------------
     * name 是 char 数组，造完之后改名字不能写 s1.name = "...",
     * 必须用 strcpy 把内容拷进去（数组名不能整体赋值）。
     * ======================================================== */
    strcpy(s1.name, "小Y同学");      /* ✅ 改字符串成员用 strcpy */
    printf("【演示2】strcpy 改名后：name=%s\n", s1.name);

    /* ========================================================
     * 演示 3：结构体能整体赋值 = ，但不能用 == 比较（对应讲义 六、七）
     * --------------------------------------------------------
     * ⭐ 同类型结构体可用 = 一次性拷贝所有成员（数组做不到！）。
     * ⚠️ 但不能用 == 比较整个结构体（编译报错），要逐成员比，
     *    字符串成员还得用 strcmp。
     * ======================================================== */
    struct Student a = {"张三", 20, 85.5};
    struct Student b;
    b = a;                           /* ✅ 整体赋值：name/age/score 一起拷过去 */
    printf("【演示3】b = a 整体赋值后：b.name=%s, b.age=%d, b.score=%.1f\n",
           b.name, b.age, b.score);
    /* 判断两张表是否相同：逐成员比，字符串用 strcmp */
    if (strcmp(a.name, b.name) == 0 && a.age == b.age && a.score == b.score) {
        printf("    逐成员比较：a 和 b 信息相同（不能写 if(a==b)）\n");
    }

    /* ========================================================
     * 演示 4：结构体指针 —— 变量用点 . ，指针用箭头 ->（对应讲义 八）
     * --------------------------------------------------------
     * ⭐⭐ 全章最高频考点。当 p = &s1 时，下面三者完全等价：
     *   s1.age（变量本体用点）
     *   (*p).age（先解引用再用点，括号不能省）
     *   p->age（指针直接用箭头，首选写法）
     * 口诀：变量用点，指针用箭头。
     * ======================================================== */
    struct Student *p = &s1;         /* p 指向 s1 */
    printf("【演示4】p->name=%s, (*p).age=%d, s1.score=%.1f（三种访问等价）\n",
           p->name, (*p).age, s1.score);
    p->score = 100.0;                /* 通过指针改成员，改的是 s1 本人 */
    printf("    p->score=100 后，s1.score=%.1f（改到了本体）\n", s1.score);

    /* ========================================================
     * 演示 5：结构体数组 + 遍历找最高分（对应讲义 九）
     * --------------------------------------------------------
     * 每个数组格子是一张填好的表，访问用"下标.成员"：class[i].score。
     * 配 for 遍历是大题最常见套路（求最值/平均/排序）。
     * ======================================================== */
    struct Student cls[3] = {
        {"张三", 20, 85.0},
        {"李四", 21, 92.0},
        {"王五", 19, 78.0}
    };
    int best = 0;                    /* 先假设第 0 个最高（打擂台） */
    for (int i = 1; i < 3; i++) {
        if (cls[i].score > cls[best].score) {
            best = i;                /* 记住更高分那个的下标 */
        }
    }
    printf("【演示5】最高分：%s（%.1f）\n", cls[best].name, cls[best].score);

    /* ========================================================
     * 演示 6：结构体作函数参数 —— 传值 vs 传指针（对应讲义 十）
     * --------------------------------------------------------
     * 传值：把整张表复制一份给函数，函数里改了影响不到外面。
     * 传指针：只传一个地址，函数用 p-> 直接改外面那张表（高效）。
     * ======================================================== */
    printf("【演示6】传值打印：");
    printStu(s1);                    /* 传整张表的复制 */
    printf("    传指针过生日前 age=%d，", s1.age);
    birthday(&s1);                   /* 传地址，函数里 age+1 改到本体 */
    printf("调用后 age=%d（指针改到了外面）\n", s1.age);

    /* ========================================================
     * 演示 7：考研格式 SqList + ElemType 认脸（对应讲义 十二点五）
     * --------------------------------------------------------
     * SqList L; 就是"造一张顺序表的空表"，等同 struct Student s1;。
     * ElemType 当 int 用，data[] 存元素，length 记当前表长。
     * ======================================================== */
    SqList L;
    L.length = 0;                    /* 空表，长度 0 */
    L.data[L.length] = 66;           /* 往表尾放一个元素 */
    L.length++;                      /* 表长 +1 */
    printf("【演示7】考研格式 SqList：插入后 length=%d, data[0]=%d\n",
           L.length, L.data[0]);

    return 0;
}

/* ============================================================
 * 函数定义
 * ============================================================ */

/* 传值：形参 s 是实参的一份完整复制，改 s 影响不到外面 */
void printStu(struct Student s)
{
    printf("%s/%d/%.1f\n", s.name, s.age, s.score);
}

/* 传指针：形参是地址，用 -> 改的是外面真正那张表 */
void birthday(struct Student *p)
{
    p->age = p->age + 1;             /* 直接改到 main 里的 s1 */
}
