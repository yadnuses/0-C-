#include <stdio.h>
#include <limits.h>

int main(void)
{
    /* ============================================================
     * Bug 1：char 的溢出 —— 127 + 1 = ?
     * 先猜：char c = 127; c++ 之后 c 等于多少？
     * ============================================================ */
    printf("=== Bug 1: char 溢出 ===\n");

    char c = 127;
    c++;
    printf("  127 + 1 = %d\n", c);  // 你猜是多少？猜对了说明你懂补码

    // 如果猜错了，自己改看看：char 能装的范围是 -128~127
    // 127 加 1 后绕回 -128（补码溢出的规律）
    // FIX: 把 c 的类型从 char 改成 int 就不会溢出了


    /* ============================================================
     * Bug 2：有符号 vs 无符号比较（考研经典坑）
     * 下面这段条件看起来像废话，但结果可能让你意外。
     * ============================================================ */
    printf("\n=== Bug 2: 有符号 vs 无符号 ===\n");

    int a = -1;
    unsigned int b = 1;
    if (a < b) {
        printf("  -1 < 1，成立\n");
    } else {
        printf("  -1 < 1，不成立？？\n");  // 猜猜这句会不会打印
    }

    // ⚠️ 原因：C 比较有符号和无符号时，会把有符号隐式转成无符号
    //   -1 转成 unsigned int 是 4294967295（最大的 unsigned int），
    //   所以 -1 < 1u 实际上是 4294967295 < 1，当然是假！
    // FIX: 把 b 的类型改成 int，或者把 a 强制转成 (unsigned int)


    /* ============================================================
     * Bug 3：整数除法陷阱 —— 为什么结果是 0？
     * 考研改错题最爱考这个。
     * ============================================================ */
    printf("\n=== Bug 3: 整数除法 ===\n");

    int x = 1, y = 3;
    double result = x / y;          // BUG：两边都是 int
    printf("  1/3 = %.10f\n", result);  // 输出是 0.0000000000 还是 0.3333333333？

    // 原因：整数除法先算 x/y = 0（砍小数），再把 0 转成 double 赋给 result
    // FIX: 改成 (double)x / y 或 x / (double)y 或 x * 1.0 / y


    /* ============================================================
     * Bug 4：sizeof 你以为你懂 char 吗？
     * 猜猜 sizeof('A') 输出几？
     * ============================================================ */
    printf("\n=== Bug 4: sizeof('A') ===\n");

    char ch = 'A';
    printf("  sizeof(char) = %zu\n", sizeof(char));   // 肯定是 1
    printf("  sizeof(ch)   = %zu\n", sizeof(ch));     // 也是 1
    printf("  sizeof('A')  = %zu\n", sizeof('A'));    // ⚠️ 你猜？

    // ⚠️ 在 C 语言里，字符常量 'A' 的类型是 int，不是 char！
    //   所以 sizeof('A') 等于 sizeof(int)，通常是 4。
    //   但在 C++ 里不一样——字符串量是 char。


    /* ============================================================
     * Bug 5：浮点数判等 —— 0.1 + 0.2 == 0.3？
     * 不用跑你也知道答案，但你知道为什么吗？
     * ============================================================ */
    printf("\n=== Bug 5: 浮点数精度 ===\n");

    double d1 = 0.1 + 0.2;
    double d2 = 0.3;
    if (d1 == d2) {
        printf("  0.1+0.2 == 0.3\n");
    } else {
        printf("  0.1+0.2 != 0.3（差值是 %.16f）\n", d1 - d2);
    }

    // FIX: 用差值比较 fabs(d1 - d2) < 1e-6


    /* ============================================================
     * Bug 6：强制类型转换 —— 小数被直接砍掉
     * 猜 (int)3.99 是多少？
     * ============================================================ */
    printf("\n=== Bug 6: 强制类型转换 ===\n");

    double pi = 3.99;
    int truncated = (int)pi;
    printf("  (int)%.2f = %d\n", pi, truncated);

    // ⚠️ C 的强制类型转换 (int) 直接砍掉小数部分，不会四舍五入！
    //   想四舍五入用 (int)(pi + 0.5)


    /* ============================================================
     * Bug 7：赋值表达式的"值"（考过无数次的坑）
     * 猜猜这个 if 判断结果是什么？
     * ============================================================ */
    printf("\n=== Bug 7: 赋值当条件 ===\n");

    int flag = 0;
    if (flag = 0) {             // BUG：一个等号是赋值！
        printf("  条件为真\n");
    } else {
        printf("  条件为假\n");
    }
    printf("  赋值后 flag = %d\n", flag);  // flag 被改成了多少？

    // ⚠️ flag = 0 是个赋值表达式，它的值就是 0（假），所以走 else
    //   但如果写的是 flag = 1，恒为真，而且 flag 还被改了！
    // FIX: 改成 flag == 0 或者 const 放左边 0 == flag


    /* ============================================================
     * Bug 8（挑战）：int 溢出回绕 —— INT_MAX + 1
     * 猜猜 INT_MAX + 1 等于多少？
     * 先用笔算，再跑程序验证。
     * ============================================================ */
    printf("\n=== Bug 8: int 溢出 ===\n");

    int max = INT_MAX;
    printf("  INT_MAX = %d\n", max);
    printf("  INT_MAX + 1 = %d\n", max + 1);

    // ⚠️ 补码加法的结果：0111...1111 + 1 = 1000...0000 = -2147483648
    //   这叫"溢出回绕"，C 标准里对有符号整数溢出是"未定义行为"，
    //   但大多数机器上会绕回 INT_MIN。


    /* ============================================================
     * Bug 9：%d 和 %u 互换 —— 同一串二进制，不同解读
     * -1 用 %u 打印是多少？4294967295 是怎么来的？
     * ============================================================ */
    printf("\n=== Bug 9: %%d vs %%u ===\n");

    int neg = -1;
    printf("  %%d 打印 -1 = %d\n", neg);
    printf("  %%u 打印 -1 = %u\n", neg);

    // ⚠️ -1 在内存里全是 1（二进制 1111...1111）：
    //   用 %d（有符号）解读 → -1
    //   用 %u（无符号）解读 → 4294967295（2^32 - 1）
    //   同一串二进制，不同解读方式得出不同数字。
    //   这就是为什么 scanf/printf 格式配错会出乱码。


    /* ============================================================
     * Bug 10（挑战）：类型转换优先级 —— (double) 在做什么？
     * 猜猜两个输出的区别。
     * ============================================================ */
    printf("\n=== Bug 10: 转型位置 ===\n");

    int m = 5, n = 2;
    double r1 = (double)(m / n);    // 先整数除法再转型
    double r2 = (double)m / n;      // 先转型再除法
    printf("  (double)(5/2) = %f\n", r1);
    printf("  (double)5 / 2 = %f\n", r2);
    printf("  两者的差 = %f\n", r2 - r1);

    // ⚠️ (double)(整数表达式) 是先算整数再转，小数早被砍了
    //   (double)整数 / 整数 是先转成 double 再除，小数保留


    printf("\n=== Debug 完毕 ===\n");
    printf("提示：这份代码跑完，建议你挨个把 BUG 修正后重新编译运行一次。\n");
    printf("      改错题考的就是【看出问题 + 写出正确写法】两步。\n");

    return 0;
}
