/* ============================================================
 * M5 数据类型陷阱 —— 配套演示程序（demo）
 * ------------------------------------------------------------
 * 这个程序把"讲义"里讲到的每个核心陷阱，都用一段可以真正
 * 跑起来的代码演示一遍。这一章是考研选择题重灾区，
 * 强烈建议你：
 *   1) 先盖住输出"猜"一遍结果；
 *   2) 再编译运行，看自己猜错在哪——错的地方正是考点。
 *
 * 编译运行（在本文件所在目录打开终端）：
 *   gcc demo.c -o demo
 *   ./demo
 *
 * 全用"固定值"演示，不需要你反复输入。练手输入请看 练习.c。
 * ============================================================ */

#include <stdio.h>

int main(void)
{
    /* ========================================================
     * 演示 1：sizeof —— 量一量每种盒子有多大（对应讲义 一）
     * --------------------------------------------------------
     * sizeof 是个运算符（不是函数），编译时就把字节数算好了。
     * 必背四个数：char 1、int 4、float 4、double 8。
     * ⚠️ sizeof 的结果类型特殊(size_t)，这里强转成 int 再用 %d 打印最稳妥。
     * ======================================================== */
    printf("【演示1】各类型字节数：char=%d int=%d float=%d double=%d\n",
           (int)sizeof(char), (int)sizeof(int),
           (int)sizeof(float), (int)sizeof(double));

    /* ========================================================
     * 演示 2：整数常量的进制陷阱（对应讲义 二）
     * --------------------------------------------------------
     * 同样写 10，前缀不同含义天差地别：
     *   10   十进制，就是 10
     *   010  ⚠️ 以 0 开头是八进制！= 1×8 = 8（不是 10）
     *   0x10 以 0x 开头是十六进制 = 1×16 = 16
     * 看到数字"前面带 0"立刻警觉是八进制。
     * ======================================================== */
    int dec = 10;
    int oct = 010;                   /* 八进制：等于十进制的 8 */
    int hex = 0x10;                  /* 十六进制：等于十进制的 16 */
    printf("【演示2】10=%d, 010=%d(八进制), 0x10=%d(十六进制)\n",
           dec, oct, hex);

    /* ========================================================
     * 演示 3：溢出回绕 —— 全章最高频考点（对应讲义 四）
     * --------------------------------------------------------
     * 盒子装不下就"绕圈"，像里程表跑到头归零，不会报错。
     *   char 最大 127，+1 绕到最小 -128；
     *   unsigned char 最大 255，+1 绕回 0；
     *   unsigned char 最小 0，-1 绕到 255。
     * ======================================================== */
    char c = 127;
    c = c + 1;                       /* 127 加 1，最高位变 1，按补码变 -128 */
    printf("【演示3】char 127+1 = %d（绕到最小值 -128）\n", c);

    unsigned char uc = 255;
    uc = uc + 1;                     /* 255 是 unsigned char 的头，再加绕回 0 */
    printf("    unsigned char 255+1 = %d（绕回 0）\n", uc);

    /* ========================================================
     * 演示 4：负数赋给 unsigned 变超大正数（对应讲义 四）
     * --------------------------------------------------------
     * -1 的补码是"全 1"，unsigned 不认符号位，把全 1 当成最大正数读。
     * 所以 unsigned int u = -1 打印出来是 42 亿多（该类型最大值）。
     * ⚠️ 打印无符号整数要用 %u，不是 %d。
     * ======================================================== */
    unsigned int u = -1;
    printf("【演示4】unsigned int u = -1，用 %%u 打印 = %u（变成最大值）\n", u);

    /* ========================================================
     * 演示 5：整数除法陷阱 + 强制转换截断（对应讲义 七）
     * --------------------------------------------------------
     * ⚠️ 两个整数相除先做整数除法（砍小数），赋给 double 也晚了：
     *   5 / 2   = 2   →  存进 double 是 2.0
     *   5 / 2.0 = 2.5 →  只要有一个是小数就按小数算
     * ⚠️⚠️ (int) 是"截断"不是"四舍五入"：(int)3.99 = 3，不是 4。
     * ======================================================== */
    int a = 5;
    double wrong = a / 2;            /* 先整数除法 5/2=2，再转 2.0 */
    double right = a / 2.0;          /* 2.0 是小数，整体按小数算 = 2.5 */
    printf("【演示5】5/2=%.1f(陷阱)  5/2.0=%.1f(正确)\n", wrong, right);
    printf("    (int)3.99 = %d，(int)9.01 = %d（直接砍小数，不进位）\n",
           (int)3.99, (int)9.01);

    /* ========================================================
     * 演示 6：字符本质是 ASCII 整数（对应讲义 六）
     * --------------------------------------------------------
     * char 存的不是"字母"而是它的 ASCII 码（整数）。
     *   同一个值：%d 看到编码，%c 看到字符。
     *   'A'=65，'a'=97，'0'=48（字符零不是数字零！）。
     * 三个必背技巧：
     *   '9'-'0'=9（字符数字转真数字）；'A'+32='a'（大小写差 32）。
     * ======================================================== */
    char ch = 'A';
    printf("【演示6】'A' 的 ASCII=%d，'A'+1 对应字符=%c\n", ch, 'A' + 1);
    printf("    '9'-'0'=%d（字符转数字）  'A'+32=%c（大写转小写）\n",
           '9' - '0', 'A' + 32);

    /* ========================================================
     * 演示 7：自增自减 ++ / -- —— 输出预测题高频（对应讲义 八）
     * --------------------------------------------------------
     * 口诀：符号在后用旧值，符号在前用新值。
     *   i++：先把旧值 5 拿去用，i 之后才变 6；
     *   ++j：先把 j 加成 6，再拿新值 6 去用。
     * ======================================================== */
    int i = 5;
    int x = i++;                     /* 先用旧值 5 给 x，i 再变 6 */
    int j = 5;
    int y = ++j;                     /* 先把 j 加成 6，再把 6 给 y */
    printf("【演示7】i++ 后：x=%d, i=%d；++j 后：y=%d, j=%d\n", x, i, y, j);

    /* ========================================================
     * 演示 8：关系运算结果是 1/0，3<=x<=5 的经典坑（对应讲义 八）
     * --------------------------------------------------------
     * 关系运算结果只有 1(真)/0(假)。
     * ⚠️ 3<=x<=5 不是数学区间！它从左到右算 = (3<=x)<=5，
     *    (3<=x) 不是 0 就是 1，永远 ≤5，所以恒为 1。
     * 想表达"x 在 3~5 之间"必须用 && 连：3<=x && x<=5。
     * ======================================================== */
    int n = 100;
    int wrongRange = (3 <= n <= 5);  /* 恒为 1，哪怕 n=100 */
    int rightRange = (3 <= n && n <= 5);  /* 正确：n=100 不在区间，结果 0 */
    printf("【演示8】n=100 时：(3<=n<=5)=%d(恒为1的坑)  (3<=n&&n<=5)=%d(正确)\n",
           wrongRange, rightRange);

    return 0;
}
