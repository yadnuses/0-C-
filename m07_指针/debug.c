#include <stdio.h>
#include <stdlib.h>

/* ---- 辅助函数：安全的 swap（正确版本，供参考） ---- */
void swap_ok(int *a, int *b)
{
    int temp = *a;
    *a = *b;
    *b = temp;
}

int main(void)
{
    /* ============================================================
     * Bug 1：最大的经典 —— swap 传值不传址
     * 函数里换的是副本，外面的变量纹丝不动。
     * ============================================================ */
    printf("=== Bug 1: swap 传值不传址 ===\n");

    int x = 3, y = 5;
    // 下面这行调用的是"传值"版本，它在讲义 demo.c 里叫 swap_bad
    // 但这里根本没定义 swap_bad，所以编译会报错
    // ⚠️ 问题：如果你写了一个 swap 函数接收 int a, int b，
    //   在函数里 a 和 b 交换了，但外面的 x 和 y 没变。
    //   因为 C 是传值调用，函数拿到的是副本。

    // 正确做法：swap_ok(&x, &y);
    swap_ok(&x, &y);
    printf("  x=%d, y=%d（应该交换成 x=5, y=3）\n", x, y);


    /* ============================================================
     * Bug 2：解引用 NULL 指针 —— 段错误（Segmentation Fault）
     * 这是新手最常见的崩溃原因。
     * ============================================================ */
    printf("\n=== Bug 2: 解引用 NULL ===\n");

    int *p = NULL;
    // BUG：下面这行会导致段错误，先注释掉看效果
    // *p = 42;     // ❌ 对空指针解引用，崩！

    // ⚠️ 使用指针前必须检查是否为空：
    if (p != NULL) {
        *p = 42;
    } else {
        printf("  p 是空指针，不能解引用。\n");
    }


    /* ============================================================
     * Bug 3：野指针 —— 指针没初始化就拿来用
     * 声明了 int *q; 没赋值，q 指向垃圾地址，解引用直接崩。
     * ============================================================ */
    printf("\n=== Bug 3: 野指针 ===\n");

    int *q;
    // BUG：下面这行会导致段错误
    // *q = 100;    // ❌ q 没初始化，指向随机地址，崩！

    q = NULL;       // 正确的做法：至少先置空
    printf("  野指针已置空，安全。\n");


    /* ============================================================
     * Bug 4：返回局部变量的地址
     * 函数结束后局部变量被销毁，返回的地址变成悬空指针。
     * 下面这个函数编译会有警告，运行会输出垃圾值或崩。
     * ============================================================ */
    printf("\n=== Bug 4: 返回局部变量地址 ===\n");

    // 先别看实现，猜猜输出是什么
    // 函数 get_local() 定义在 main 后面，去看一眼

    // BUG：这行编译有警告，运行结果不可预测
    // int *r = get_local();
    // printf("  get_local() 返回的地址里的值 = %d\n", *r);

    printf("  （注释掉了，你自己改一下取消注释试试）\n");

    // ⚠️ 解决方法：要么用 static，要么用 malloc
    //   static int val = 42; return &val;
    //   或 int *p = malloc(sizeof(int)); *p = 42; return p;


    /* ============================================================
     * Bug 5：数组名不是指针变量 —— 不能自增
     * 数组名是常量地址，不能 a++，但可以 *(a+1)
     * ============================================================ */
    printf("\n=== Bug 5: 数组名不能++ ===\n");

    int arr[3] = {10, 20, 30};
    // BUG：下面这行编译报错
    // arr++;       // ❌ 数组名是 const 地址，不能自增

    // 正确做法：用指针变量
    int *pa = arr;
    pa++;  // 现在 pa 指向 arr[1]
    printf("  arr[1] = %d（通过指针++访问）\n", *pa);


    /* ============================================================
     * Bug 6：字符串常量不能改
     * char *s = "hello"; 试图修改 s[0] 是未定义行为（通常崩）
     * ============================================================ */
    printf("\n=== Bug 6: 字符串常量不可修改 ===\n");

    char *str = "hello";   // "hello" 存在只读区
    // BUG：下面这行会崩
    // str[0] = 'H';       // ❌ 试图改只读内存

    // 正确做法：用字符数组
    char str2[] = "hello";
    str2[0] = 'H';
    printf("  改为字符数组后: %s\n", str2);


    /* ============================================================
     * Bug 7：指针类型不匹配 —— int * 和 char *
     * ============================================================ */
    printf("\n=== Bug 7: 指针类型不匹配 ===\n");

    int val = 0x12345678;  // 十六进制：一个 int 占 4 字节
    char *pc = (char *)&val;  // 强制转成 char *

    // 用 char 指针看 int，每次只读 1 个字节
    printf("  int 0x%x 的字节序列（小端）：", val);
    for (int i = 0; i < 4; i++) {
        printf("%02x ", (unsigned char)pc[i]);
    }
    printf("\n");
    // ⚠️ 小端序：低位字节在低地址


    /* ============================================================
     * Bug 8：scanf 丢 &（M1 就学过的坑，但指针学完理解更深）
     * 现在你知道 scanf 要 & 的原因了——它要的是"地址"。
     * ============================================================ */
    printf("\n=== Bug 8: scanf 缺 & ===\n");

    int num = 0;
    printf("  请输入一个整数: ");
    // BUG：下面这行去掉注释会怎样？
    // scanf("%d", num);       // ❌ 没加 &，拿 num 的值当地址，崩！
    scanf("%d", &num);          // ✅ 正确
    printf("  你输入了: %d\n", num);


    /* ============================================================
     * Bug 9（进阶）：指针算术 —— 指向越界
     * 指针可以指向数组之外，解引用它就是越界访问。
     * ============================================================ */
    printf("\n=== Bug 9: 指针越界 ===\n");

    int nums[3] = {1, 2, 3};
    int *pn = nums;

    // 合法访问：
    for (int i = 0; i < 3; i++) {
        printf("  nums[%d] = %d\n", i, *(pn + i));
    }

    // BUG：越界访问
    printf("  越界：*(pn+3) = %d（可能是垃圾值）\n", *(pn + 3));
    // ⚠️ 编译器不检查越界，运行时可能读到垃圾值或崩。
    //   *pn-3, *(pn+100000) 都有可能，完全取决于运气。


    /* ============================================================
     * Bug 10（挑战）：二级指针 vs 一级指针混淆
     * 想在函数里修改指针本身（不是指针指向的值），要传指针的指针。
     * ============================================================ */
    printf("\n=== Bug 10: 修改指针本身需要二级指针 ===\n");

    int value = 42;
    int *ptr = NULL;

    printf("  ptr 现在是: %s\n", ptr == NULL ? "NULL" : "非空");

    // 假设有一个函数 set_ptr(&ptr, &value)，想让 ptr 指向 value
    // BUG：如果 set_ptr 定义成 void set_ptr(int *p, int *v)，
    //   传入 set_ptr(ptr, &value)，你猜 ptr 变没变？

    // ⚠️ 想改指针本身，函数参数必须是 int **p
    //   调用时传 &ptr

    printf("\n=== Debug 完毕 ===\n");
    printf("提示：把所有注释掉的 BUG 行取消注释，看看哪些会崩，哪些敢警告。\n");
    printf("      段错误不可怕，可怕的是不知道为什么会段错误。\n");

    return 0;
}

