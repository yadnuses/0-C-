/* ============================================================
 * M3 循环结构 —— 练习答案（对照参考）
 * ------------------------------------------------------------
 * 每道题给了至少一种写法。同一道题可能有多种实现，这里只列
 * 一种最清晰的版本。大部分题都有两种写法（for / while）。
 * ============================================================ */

#include <stdio.h>

int main(void)
{
    /* ============================================================
     * 练习1：求 1~n 所有偶数的和
     * ============================================================ */
    int n;
    printf("练习1：求 1~n 所有偶数的和\n");
    printf("输入 n: ");
    scanf("%d", &n);

    int sum = 0;
    for (int i = 1; i <= n; i++) {
        if (i % 2 == 0) {
            sum += i;
        }
    }
    printf("1~%d 所有偶数和 = %d\n\n", n, sum);


    /* ============================================================
     * 练习2：判断素数
     * ============================================================ */
    int num;
    printf("练习2：判断素数\n");
    printf("输入一个整数: ");
    scanf("%d", &num);

    if (num <= 1) {
        printf("%d 不是素数\n\n", num);
    } else {
        int isPrime = 1;
        for (int i = 2; i <= num / 2; i++) {
            if (num % i == 0) {
                isPrime = 0;
                break;
            }
        }
        if (isPrime) {
            printf("%d 是素数\n\n", num);
        } else {
            printf("%d 不是素数\n\n", num);
        }
    }


    /* ============================================================
     * 练习3：打印直角三角形
     * ============================================================ */
    int rows;
    printf("练习3：打印直角三角形\n");
    printf("输入行数: ");
    scanf("%d", &rows);

    for (int i = 1; i <= rows; i++) {
        for (int j = 1; j <= i; j++) {
            printf("*");
        }
        printf("\n");
    }
    printf("\n");


    /* ============================================================
     * 练习4：continue + break 预测
     * 预测结果：0 1 3
     * 推理：i=0→打印0→i=1→打印1→i=2→continue（跳过打印）→
     *       i=3→打印3→i=4→break（直接退出）
     * ============================================================ */
    printf("练习4 的实际输出：");
    for (int i = 0; i < 5; i++) {
        if (i == 2) continue;
        if (i == 4) break;
        printf("%d ", i);
    }
    printf("\n\n");


    /* ============================================================
     * 练习5：用 while 重写偶数求和
     * ============================================================ */
    printf("练习5：用 while 重写偶数求和\n");

    int sum2 = 0, j = 1;
    while (j <= n) {
        if (j % 2 == 0) {
            sum2 += j;
        }
        j++;
    }
    printf("while 版 1~%d 偶数和 = %d\n\n", n, sum2);


    /* ============================================================
     * 练习6：do-while 统计整数位数
     * ============================================================ */
    int number;
    printf("练习6：统计整数位数\n");
    printf("输入一个整数: ");
    scanf("%d", &number);

    int count = 0;
    if (number < 0) {
        number = -number;
    }
    do {
        number /= 10;
        count++;
    } while (number != 0);
    printf("位数 = %d\n\n", count);


    /* ============================================================
     * 练习7：1! + 2! + ... + n!
     * ============================================================ */
    int m;
    printf("练习7：求 1! + 2! + ... + n!\n");
    printf("输入 n: ");
    scanf("%d", &m);

    int total_sum = 0;
    for (int i = 1; i <= m; i++) {
        int fact = 1;
        for (int j = 1; j <= i; j++) {
            fact *= j;
        }
        total_sum += fact;
    }
    printf("1! + 2! + ... + %d! = %d\n\n", m, total_sum);


    /* ============================================================
     * 练习8：水仙花数
     * ============================================================ */
    printf("练习8：水仙花数（三位数）：");

    for (int i = 100; i <= 999; i++) {
        int ge = i % 10;
        int shi = i / 10 % 10;
        int bai = i / 100;
        if (bai * bai * bai + shi * shi * shi + ge * ge * ge == i) {
            printf("%d ", i);
        }
    }
    printf("\n\n");


    /* ============================================================
     * 练习9：斐波那契数列前 n 项
     * ============================================================ */
    int fn;
    printf("练习9：斐波那契数列前 n 项\n");
    printf("输入 n（>= 2）: ");
    scanf("%d", &fn);

    int x1 = 1, x2 = 1, x;
    printf("前 %d 项：%d %d ", fn, x1, x2);
    for (int i = 3; i <= fn; i++) {
        x = x1 + x2;
        printf("%d ", x);
        x1 = x2;
        x2 = x;
    }
    printf("\n\n");


    /* ============================================================
     * 练习10：百钱买百鸡
     * 公鸡5文、母鸡3文、小鸡三只1文；100文买100只
     * ============================================================ */
    printf("练习10：百钱买百鸡\n");

    for (int cock = 0; cock <= 20; cock++) {
        for (int hen = 0; hen <= 33; hen++) {
            int chick = 100 - cock - hen;
            if (chick >= 0 &&
                cock * 5 + hen * 3 + chick / 3.0 == 100) {
                printf("  公鸡%d只, 母鸡%d只, 小鸡%d只\n",
                       cock, hen, chick);
            }
        }
    }

    return 0;
}
