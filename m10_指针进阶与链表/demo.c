/* ============================================================
 * M10 指针进阶与链表 —— 配套演示程序（demo）
 * ------------------------------------------------------------
 * 链表是考研大题命门（年年考插入/删除）。这个程序把"讲义"里
 * 的每个核心考点都用可运行代码演示一遍，每根指针的动作都配
 * 了注释。建议你：
 *   1) 先读讲义对应小节（边读边画箭头图），再回来看这里的演示；
 *   2) 编译运行，亲眼看到链表变化，再对照注释里说的"为什么"。
 *
 * 编译运行（在本文件所在目录打开终端）：
 *   gcc demo.c -o demo
 *   ./demo
 *
 * 全用"固定值"演示，不需要你反复输入。练手输入请看 练习.c。
 * ============================================================ */

#include <stdio.h>
#include <stdlib.h>      /* malloc/calloc/free 在这个头文件里 */

/* ============================================================
 * 链表节点定义 —— 对应讲义 三
 * ------------------------------------------------------------
 * 每个节点两个域：数据域 data + 指针域 next。
 * ⚠️ next 是"指向同类型节点的指针"，靠它把节点串起来。
 *    struct Node 还没定义完就用 struct Node* 是合法的，
 *    因为指针大小固定，编译器不需要知道完整结构。
 * ============================================================ */
struct Node {
    int data;                        /* 数据域：存的值 */
    struct Node *next;               /* 指针域：指向下一个节点 */
};

/* 函数声明 */
struct Node *makeNode(int x);                /* 造一个节点 */
void printList(struct Node *head);           /* 遍历打印 */
int  listLength(struct Node *head);          /* 求长度 */
struct Node *deleteValue(struct Node *head, int x);  /* 删除指定值（含删头特例） */
void freeList(struct Node *head);            /* 释放整条链表 */

int main(void)
{
    /* ========================================================
     * 演示 1：动态内存 malloc —— 当数组用（对应讲义 一）
     * --------------------------------------------------------
     * 普通数组大小编译时定死；malloc 运行时按需申请。
     * 标准五步：申请 → 检查NULL → 使用 → 释放 → 置空。
     * ⚠️ malloc 返回 void*，要强转；用 sizeof(类型)*个数 算大小，
     *    别写死字节数。malloc 不清零（垃圾值），calloc 才清零。
     * ======================================================== */
    int n = 5;
    int *arr = (int *)malloc(sizeof(int) * n);   /* 申请 5 个 int */
    if (arr == NULL) {                           /* 必须检查是否申请成功 */
        printf("内存申请失败\n");
        return 1;
    }
    for (int i = 0; i < n; i++) {
        arr[i] = (i + 1) * 10;                   /* 当普通数组用，下标写法一样 */
    }
    printf("【演示1】malloc 动态数组：");
    for (int i = 0; i < n; i++) {
        printf("%d ", arr[i]);
    }
    printf("\n");
    free(arr);                                   /* 用完释放，否则内存泄漏 */
    arr = NULL;                                  /* 置空，防野指针 */

    /* ========================================================
     * 演示 2：二级指针 —— 指针的地址（对应讲义 二）
     * --------------------------------------------------------
     * pp 装 p 的地址，p 装 num 的地址：
     *   *pp 剥一层 = p；**pp 剥两层 = num 的值。
     * 用途：想在函数里改"指针本身"，就传二级指针（头插法伏笔）。
     * ======================================================== */
    int num = 10;
    int *p = &num;                   /* 一级指针指向 num */
    int **pp = &p;                   /* 二级指针指向 p */
    printf("【演示2】num=%d, *p=%d, **pp=%d（剥两层皮拿到同一个值）\n",
           num, *p, **pp);

    /* ========================================================
     * 演示 3：造节点 + 手动串成链表 + 遍历（对应讲义 三）
     * --------------------------------------------------------
     * 造节点三步：malloc → 填 data → next 置 NULL。
     * "串"的本质：前一个节点的 next 装着后一个节点的地址。
     * 遍历套路：cur 从 head 出发，while(cur) 里 cur=cur->next，
     * ⚠️ 挪步只能 cur=cur->next，绝不能 cur++（节点不连续）。
     * ======================================================== */
    struct Node *head = makeNode(1);
    head->next = makeNode(2);                    /* 1 拉住 2 */
    head->next->next = makeNode(3);              /* 2 拉住 3 */
    printf("【演示3】手动串成的链表：");
    printList(head);                             /* 输出 1 2 3 */

    /* ========================================================
     * 演示 4：头插法建表 —— 结果是逆序（对应讲义 三）
     * --------------------------------------------------------
     * 核心两句，顺序绝不能反：
     *   newNode->next = head;  新节点先拉住旧头
     *   head = newNode;        头指针再指向新节点
     * ⚠️ 反了会丢失旧头、整条链断掉。
     * 把 1、2、3 依次头插，结果是 3→2→1（后进的在最前，逆序）。
     * ======================================================== */
    struct Node *h2 = NULL;          /* 空表起步 */
    for (int i = 1; i <= 3; i++) {
        struct Node *newNode = makeNode(i);
        newNode->next = h2;          /* ① 新节点先接住旧头 */
        h2 = newNode;                /* ② 头指针再移到新节点 */
    }
    printf("【演示4】头插 1,2,3 的结果（逆序）：");
    printList(h2);                   /* 输出 3 2 1 */

    /* ========================================================
     * 演示 5：在中间插入节点 —— 先连后断（对应讲义 三）
     * --------------------------------------------------------
     * 🎯 大题最爱考。在 prev 后面插入 s 的标准两句：
     *   s->next = prev->next;   ① 新节点先拉住 prev 的后继
     *   prev->next = s;         ② prev 再改拉新节点
     * ⚠️ 顺序反了（先断）会丢失后半截链。
     * 这里在节点 1 和 2 之间插入 99。
     * ======================================================== */
    struct Node *prev = head;        /* prev 指向节点 1 */
    struct Node *s = makeNode(99);
    s->next = prev->next;            /* ① 99 先拉住 prev 原来的后继（节点2） */
    prev->next = s;                  /* ② 节点1 改拉 99 */
    printf("【演示5】在 1 和 2 之间插入 99：");
    printList(head);                 /* 输出 1 99 2 3 */

    /* ========================================================
     * 演示 6：删除指定值节点 —— 先接后删（对应讲义 三）
     * --------------------------------------------------------
     * 🎯 大题常客。前驱跨过被删节点，再 free：
     *   prev->next = del->next;  ① 前驱直接拉住被删节点的后继
     *   free(del);               ② 释放被删节点
     * ⚠️ 删的若是头节点没有前驱，要单独更新 head（见函数实现）。
     * 这里删掉刚插入的 99。
     * ======================================================== */
    head = deleteValue(head, 99);    /* 删除值为 99 的节点 */
    printf("【演示6】删除 99 后：");
    printList(head);                 /* 输出 1 2 3 */
    printf("    链表长度 = %d\n", listLength(head));

    /* ========================================================
     * 演示 7：释放整条链表 —— 防内存泄漏（对应讲义 三）
     * --------------------------------------------------------
     * ⚠️ 释放当前节点前，必须先用临时指针记住下一个，
     *    否则 free 完就找不到后面了（新手最常翻的车）。
     * ======================================================== */
    freeList(head);
    freeList(h2);
    printf("【演示7】两条链表已全部释放（malloc 配 free，不泄漏）\n");

    return 0;
}

/* ============================================================
 * 函数定义
 * ============================================================ */

/* 造一个节点：申请内存 → 填数据 → next 置 NULL */
struct Node *makeNode(int x)
{
    struct Node *p = (struct Node *)malloc(sizeof(struct Node));
    p->data = x;
    p->next = NULL;                  /* 新节点 next 一定先置空 */
    return p;
}

/* 遍历打印：cur 从 head 走到 NULL */
void printList(struct Node *head)
{
    struct Node *cur = head;
    while (cur != NULL) {            /* 到 NULL 表示走到尽头 */
        printf("%d ", cur->data);
        cur = cur->next;             /* ⚠️ 挪步：不能写 cur++ */
    }
    printf("\n");
}

/* 求长度：遍历数节点个数 */
int listLength(struct Node *head)
{
    int count = 0;
    for (struct Node *cur = head; cur != NULL; cur = cur->next) {
        count++;
    }
    return count;                    /* 空表返回 0，天然正确 */
}

/* 删除第一个值为 x 的节点，返回新的 head */
struct Node *deleteValue(struct Node *head, int x)
{
    if (head == NULL) {
        return NULL;                 /* 空表，没啥可删 */
    }
    if (head->data == x) {           /* 特例：要删的正是头节点 */
        struct Node *del = head;
        head = head->next;           /* head 后移，跳过第一个 */
        free(del);                   /* 释放原来的头 */
        return head;
    }
    struct Node *prev = head;        /* 找被删节点的前驱 */
    while (prev->next != NULL && prev->next->data != x) {
        prev = prev->next;
    }
    if (prev->next != NULL) {        /* 找到了 */
        struct Node *del = prev->next;
        prev->next = del->next;      /* ① 先接：前驱跨过被删节点 */
        free(del);                   /* ② 后删：释放被删节点 */
    }
    return head;
}

/* 释放整条链表 */
void freeList(struct Node *head)
{
    while (head != NULL) {
        struct Node *del = head;
        head = head->next;           /* ⚠️ 先记住下一个，再 free 当前 */
        free(del);
    }
}
