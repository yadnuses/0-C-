/* ============================================================
 * M6 数组与字符串 —— 配套演示程序（demo）
 * ------------------------------------------------------------
 * 这个程序把"讲义"里讲到的每个核心考点，都用一段可以真正
 * 跑起来的代码演示一遍。建议你：
 *   1) 先读讲义对应小节，再回来看这里对应的演示；
 *   2) 编译运行，亲眼看到输出，再回头对照注释里说的"为什么"。
 *
 * 编译运行（在本文件所在目录打开终端）：
 *   gcc demo.c -o demo
 *   ./demo
 *
 * 全用"固定值"演示，不需要你反复输入。练手输入请看 练习.c。
 * ============================================================ */

#include <stdio.h>
#include <string.h>      /* strlen/strcpy/strcmp/strcat 必须包含这个头文件 */

int main(void)
{
    /* ========================================================
     * 演示 1：一维数组的下标 —— 全场最大的坑（对应讲义 一）
     * --------------------------------------------------------
     * int a[5] 造了 5 个盒子，门牌号是 0、1、2、3、4。
     * ⚠️ 下标从 0 开始！第 1 个是 a[0]，最后一个是 a[4]，
     *    根本没有 a[5]（写了就越界，C 还不报错，很危险）。
     * 合法下标范围永远是 0 ~ n-1。
     * ======================================================== */
    int a[5] = {10, 20, 30, 40, 50};
    printf("【演示1】第1个 a[0]=%d，最后一个 a[4]=%d（没有 a[5]）\n",
           a[0], a[4]);

    /* ========================================================
     * 演示 2：初始化补 0 规则（对应讲义 一）
     * --------------------------------------------------------
     * 大括号给的值不够数组长度时，剩下的盒子自动补 0。
     *   {1,2,3} 给 5 个盒子 → 后两个 b[3]、b[4] 自动是 0。
     * 高频考点：int b[5]={1,2,3}; 问 b[4] 是几，答案是 0。
     * ======================================================== */
    int b[5] = {1, 2, 3};            /* 等价于 {1,2,3,0,0} */
    printf("【演示2】b[5]={1,2,3}：b[2]=%d, b[3]=%d, b[4]=%d（不够的补0）\n",
           b[2], b[3], b[4]);

    /* ========================================================
     * 演示 3：遍历 + 三大套路（求和/平均、最值、逆序）（对应讲义 三、四）
     * --------------------------------------------------------
     * 遍历配 for，长度 5 就用 i<5（不是 i<=5，否则碰 a[5] 越界）。
     * ⚠️ 求平均要除 5.0（带小数），写 5 会整数除法砍掉小数。
     * 求最值用"打擂台"：先让第一个当擂主，后面更大就替换。
     * ======================================================== */
    int sum = 0;
    int max = a[0];                  /* 先假设第一个最大（打擂台） */
    for (int i = 0; i < 5; i++) {    /* i 从 0 到 4 */
        sum = sum + a[i];            /* 累加器：先清 0 再逐个加 */
        if (a[i] > max) {            /* 来挑战的比擂主大 */
            max = a[i];              /* 换擂主 */
        }
    }
    printf("【演示3】和=%d，平均=%.1f（除5.0），最大值=%d\n",
           sum, sum / 5.0, max);

    /* ========================================================
     * 演示 4：二维数组 —— 表格 + 双重循环遍历（对应讲义 五）
     * --------------------------------------------------------
     * m[2][3]：2 行 3 列，用两个下标 m[行][列]，行列都从 0 起。
     * 内存按"行优先"连续存放（先存完第 0 行，再存第 1 行）。
     * 遍历用双重循环：外层走行 i，内层走列 j，每行打完换行。
     * ======================================================== */
    int m[2][3] = {
        {1, 2, 3},                   /* 第 0 行 */
        {4, 5, 6}                    /* 第 1 行 */
    };
    printf("【演示4】二维数组 2行3列：\n");
    for (int i = 0; i < 2; i++) {    /* 外层：行 */
        printf("    ");
        for (int j = 0; j < 3; j++) {/* 内层：列 */
            printf("%d ", m[i][j]);
        }
        printf("\n");                /* 一整行打完，换行 */
    }

    /* ========================================================
     * 演示 5：字符串 = 字符数组 + 结尾 '\0'（对应讲义 六、十）
     * --------------------------------------------------------
     * C 没有字符串类型，字符串就是 char 数组，末尾自动加 '\0'。
     * ⚠️⚠️ "Hello" 看着 5 个字母，实际占 6 字节（含 '\0'）。
     * strlen vs sizeof（最爱考的一对）：
     *   strlen 数 '\0' 之前的有效字符 → 5（不含 '\0'）；
     *   sizeof 数整个数组的总字节数  → 6（含那个 '\0'）。
     * ======================================================== */
    char s[] = "Hello";              /* 编译器自动在末尾加 '\0' */
    printf("【演示5】\"Hello\"：strlen=%d（有效字符,不含\\0），sizeof=%d（总字节,含\\0）\n",
           (int)strlen(s), (int)sizeof(s));

    /* ========================================================
     * 演示 6：遍历字符串靠 '\0' 停（对应讲义 六）
     * --------------------------------------------------------
     * 字符串不靠"长度 n"遍历，而是检测 '\0'：
     *   循环条件 s[i] != '\0' —— 还没碰到结束标志就继续。
     * 走到 '\0' 自动停，不会打印出 '\0'。
     * ======================================================== */
    printf("【演示6】逐字符遍历 \"Hello\"：");
    for (int i = 0; s[i] != '\0'; i++) {   /* 碰到 '\0' 就停 */
        printf("%c ", s[i]);
    }
    printf("\n");

    /* ========================================================
     * 演示 7：字符串三大铁律 —— 用函数而非 == / =（对应讲义 七）
     * --------------------------------------------------------
     * ⚠️⚠️ 改错题重灾区：
     *   比较内容用 strcmp（相等返回 0！不是 1），不能用 ==
     *   （== 比的是两个数组的地址，永远不相等）；
     *   复制内容用 strcpy，不能用 =（数组名不能整体赋值）。
     * ======================================================== */
    char p1[] = "abc";
    char p2[] = "abc";
    printf("【演示7】strcmp(\"abc\",\"abc\")=%d（相等返回0）：",
           strcmp(p1, p2));
    if (strcmp(p1, p2) == 0) {       /* ✅ 正确：比内容用 strcmp，判 ==0 */
        printf("内容相同\n");
    }

    char dst[20];
    strcpy(dst, "Hi");               /* ✅ 复制用 strcpy，不能写 dst = "Hi" */
    strcat(dst, " C");               /* 拼接：把 " C" 接到屁股后面 */
    printf("    strcpy 后再 strcat：dst = \"%s\"\n", dst);

    /* ========================================================
     * 演示 8：字符就是小整数 —— 数字字符减 '0'（对应讲义 八）
     * --------------------------------------------------------
     * 字符底层是 ASCII 整数，能直接参与加减。
     * 经典技巧：把字符串里的数字字符逐位拼成整数：
     *   number = number*10 + (字符 - '0')
     * '7'-'0' = 55-48 = 7，得到字符代表的真实数值。
     * ======================================================== */
    char digits[] = "123";
    int number = 0;
    for (int i = 0; digits[i] != '\0'; i++) {
        number = number * 10 + (digits[i] - '0');  /* 逐位拼：1 → 12 → 123 */
    }
    printf("【演示8】把字符串 \"123\" 拼成整数 = %d（每位减 '0'）\n", number);

    return 0;
}
