/* ============================================================
 * M4 函数 —— 配套演示程序（demo）
 * ------------------------------------------------------------
 * 这个程序把"讲义"里讲到的每个核心考点，都用一段可以真正
 * 跑起来的代码演示一遍。建议你：
 *   1) 先读讲义对应小节，再回来看这里对应的演示；
 *   2) 编译运行，亲眼看到输出，再回头对照注释里说的"为什么"。
 *
 * 编译运行（在本文件所在目录打开终端）：
 *   gcc demo.c -o demo
 *   ./demo
 *
 * 全用"固定值"演示，不需要你反复输入。练手输入请看 练习.c。
 * ============================================================ */

#include <stdio.h>

/* ============================================================
 * 函数声明（原型）—— 对应讲义 六（M4 头号考点）
 * ------------------------------------------------------------
 * 电脑从上往下读代码。下面这些函数都定义在 main 后面，所以
 * 必须先在 main 前面"打个招呼"，告诉电脑它们的样子，否则
 * 读到 main 里的调用时电脑还不认识它们，会报错。
 * ⚠️⚠️ 声明结尾有分号 ;（下面的定义则没有分号）——这是
 *      改错题年年考的点：声明有分号，定义没分号。
 * ============================================================ */
int add(int a, int b);      /* 要两个 int，还一个 int */
int isEven(int n);          /* 还 1 表示偶数，还 0 表示奇数 */
void sayLine(void);         /* void：不还值；(void)：不要参数 */
void swap(int a, int b);    /* 经典"假交换"，演示传值调用 */
void counter(void);         /* 演示 static 静态局部变量 */
int fact(int n);            /* 递归求阶乘 */

int main(void)
{
    /* ========================================================
     * 演示 1：带返回值的函数 + 调用执行流程（对应讲义 三、四）
     * --------------------------------------------------------
     * 调用 add(3,5) 时，main 暂停，程序"跳"进 add 算出 8，
     * return 把 8 带回调用处，整行就相当于拿到了 8。
     * 返回值可以直接丢给 printf，也能参与运算。
     * ======================================================== */
    printf("【演示1】add(3,5)=%d，还能参与运算 add(1,2)+add(10,20)=%d\n",
           add(3, 5), add(1, 2) + add(10, 20));

    /* ========================================================
     * 演示 2：用返回值当判断条件（对应讲义 四）
     * --------------------------------------------------------
     * isEven 里有两个 return，根据 n 的奇偶走其中一个出去。
     * 一个函数里可以有多个 return，走到哪个就从哪个收工。
     * ======================================================== */
    int x = 7;
    printf("【演示2】判断 %d：", x);
    if (isEven(x)) {                 /* isEven 还回 1(真)/0(假)，直接当条件用 */
        printf("是偶数\n");
    } else {
        printf("是奇数\n");          /* 7 是奇数，走这里 */
    }

    /* ========================================================
     * 演示 3：void 函数（对应讲义 五）
     * --------------------------------------------------------
     * void 函数不还任何值，所以只能"单独成一句"调用，
     * 不能写成 int y = sayLine(); 因为它没东西给你接。
     * ======================================================== */
    printf("【演示3】调用 void 函数画条线：\n");
    sayLine();                       /* ✅ 单独成句 */

    /* ========================================================
     * 演示 4：传值调用 —— 函数改不动外面的变量（对应讲义 七）
     * --------------------------------------------------------
     * ⚠️⚠️ 全场最经典的考点（swap 假交换）：
     * 调用 swap(p,q) 时，把 p、q 的值"复印"给形参 a、b。
     * 函数里换的是复印件 a、b，外面的原件 p、q 一根毛都没动。
     * 要真正改外面的变量，得传地址/指针（M7 讲）。
     * ======================================================== */
    int p = 3, q = 5;
    printf("【演示4】交换前：p=%d, q=%d\n", p, q);
    swap(p, q);                      /* 函数内部确实换了，但换的是复印件 */
    printf("    交换后：p=%d, q=%d（没变！因为传的是值的复印件）\n", p, q);

    /* ========================================================
     * 演示 5：static 静态局部变量 —— 能记住上次的值（对应讲义 九）
     * --------------------------------------------------------
     * 普通局部变量每次进函数都重新出生，记不住上次。
     * static 局部变量只初始化一次，之后记住上次留下的值，
     * 所以连调三次会打印 1、2、3 而不是 1、1、1。
     * ======================================================== */
    printf("【演示5】连续调用 static 计数函数三次：");
    counter();                       /* 1 */
    counter();                       /* 2（记住了上次的 1） */
    counter();                       /* 3 */
    printf("\n");

    /* ========================================================
     * 演示 6：递归 —— 函数调用自己（对应讲义 十一）
     * --------------------------------------------------------
     * fact(5) = 5 * fact(4) = 5*4*fact(3) = ... 一层层缩小，
     * 缩到 fact(1)=1 这个"终止条件"才停，再一层层往回乘。
     * ⚠️ 递归必须有终止条件，否则无限调用自己直到崩溃。
     * ======================================================== */
    printf("【演示6】递归求 5! = %d\n", fact(5));

    return 0;
}

/* ============================================================
 * 下面是函数定义 —— ⚠️ 首部后面都没有分号，都带 { } 函数体。
 * 对比上面的声明：声明有分号、无函数体；定义无分号、有函数体。
 * ============================================================ */

/* 形参类型必须各写各的：int a, int b（不能写成 int a, b） */
int add(int a, int b)
{
    return a + b;                    /* 算出和，带回调用处 */
}

int isEven(int n)
{
    if (n % 2 == 0) {
        return 1;                    /* 偶数还 1 */
    }
    return 0;                        /* 否则还 0（一个函数里两个 return） */
}

/* 前一个 void = 无返回值；括号里 void = 无参数 */
void sayLine(void)
{
    printf("    ------------------\n");
    /* void 函数没有 return 值；这里也可以写光秃秃的 return; 提前结束 */
}

void swap(int a, int b)
{
    int t = a;                       /* a、b 是形参（局部变量），是外面实参的复印件 */
    a = b;
    b = t;                           /* 复印件确实换了，但函数一结束 a、b 就销毁 */
    printf("    （函数内部 a=%d, b=%d，复印件确实换了）\n", a, b);
}

void counter(void)
{
    static int n = 0;                /* ⚠️ static：只在第一次调用时初始化为 0 */
    n = n + 1;                       /* 之后每次进来都用上次留下的 n，累加 */
    printf("%d ", n);
}

int fact(int n)
{
    if (n <= 1) {
        return 1;                    /* 终止条件：缩到最简单情况，直接给答案 */
    }
    return n * fact(n - 1);          /* 自己调用更小一号的自己 */
}
