/* ============================================================
 * M3 循环结构 —— 配套演示程序（demo）
 * ------------------------------------------------------------
 * 这个程序把"讲义"里讲到的每个核心考点，都用一段可以真正
 * 跑起来的代码演示一遍。建议你：
 *   1) 先读讲义对应小节，再回来看这里对应的演示；
 *   2) 编译运行，亲眼看到输出，再回头对照注释里说的"为什么"。
 *
 * 编译运行（在本文件所在目录打开终端）：
 *   gcc demo.c -o demo
 *   ./demo
 *
 * 为了让你一次运行就看到所有考点，这里全用"固定值"演示，
 * 不需要你反复输入。真正练手输入请看同目录的 练习.c。
 * ============================================================ */

#include <stdio.h>

int main(void)
{
    /* ========================================================
     * 演示 1：for 循环的执行顺序（对应讲义 二）
     * --------------------------------------------------------
     * 括号里两个分号分成三段：初始化; 条件; 步进。
     * 执行顺序（必背）：
     *   ① 初始化 int i=0   ← 整个循环只执行 1 次
     *   ② 判断条件 i<3     ← 每轮开始先判断
     *   ③ 执行循环体        ← 条件为真才执行
     *   ④ 步进 i++         ← 循环体干完才执行，然后回到 ②
     * 注意：i 从 0 到 2，条件是 i<3，一共跑 3 次（不是 4 次）。
     * ======================================================== */
    printf("【演示1】for 循环跑 3 次：\n");
    for (int i = 0; i < 3; i++) {
        printf("    第 %d 次（i 现在是 %d）\n", i + 1, i);
    }

    /* ========================================================
     * 演示 2：三个核心套路 —— 累加 / 计数 / 累乘（对应讲义 三）
     * --------------------------------------------------------
     * ⚠️ 致命区别（考试最爱挖坑）：
     *   求和的存钱罐初值是 0（加 0 不影响结果）；
     *   求积的存钱罐初值是 1（若写 0，0 乘任何数都是 0，结果恒为 0）。
     * 口诀：加法从 0 起，乘法从 1 起。
     * ======================================================== */
    int sum = 0;                         /* 累加器：初值必须 0 */
    int count = 0;                       /* 计数器：初值 0 */
    int product = 1;                     /* 累乘器：初值必须 1 */
    for (int i = 1; i <= 5; i++) {
        sum = sum + i;                   /* 1+2+3+4+5 */
        if (i % 2 == 0) {                /* i 是偶数吗：除 2 余 0 */
            count = count + 1;           /* 数一数 1~5 里有几个偶数 */
        }
        product = product * i;           /* 1×2×3×4×5 = 5! */
    }
    printf("【演示2】1~5 求和=%d，偶数个数=%d，5的阶乘=%d\n",
           sum, count, product);

    /* ========================================================
     * 演示 3：while 循环 + 数位分解（对应讲义 四、五）
     * --------------------------------------------------------
     * while 括号里只有条件，初始化和步进要自己在别处写。
     * 数位分解两招（高频）：
     *   n % 10 取出最后一位（个位）；
     *   n / 10 去掉最后一位（整数除法砍掉小数）。
     * 这里用它来"逆序输出"一个整数。
     * ======================================================== */
    int x = 12534;
    printf("【演示3】逆序输出 %d：", x);
    while (x != 0) {                     /* x 变成 0 就停 */
        printf("%d", x % 10);            /* 打印末位：4,3,5,2,1 */
        x = x / 10;                      /* 去掉末位：1253,125,12,1,0 */
    }
    printf("\n");

    /* ========================================================
     * 演示 4：while vs do-while —— 最高频对比（对应讲义 五）
     * --------------------------------------------------------
     * 同样的条件 i<5、同样的 i=10：
     *   while   先判断后执行 → 一次都不跑（0 次）；
     *   do-while 先执行后判断 → 怎么着也跑 1 次（至少 1 次）。
     * ⚠️ do-while 末尾的 while(...) 后面有分号！while 循环没有。
     * ======================================================== */
    printf("【演示4】while vs do-while（都用 i=10, 条件 i<5）：\n");
    int i = 10;
    while (i < 5) {
        printf("    while 跑了（这句不会出现）\n");
    }
    printf("    while：一次没跑（先判断，10<5 为假）\n");

    i = 10;
    do {
        printf("    do-while：先跑了 1 次（先执行才判断）\n");
    } while (i < 5);

    /* ========================================================
     * 演示 5：break 与 continue 的区别（对应讲义 六）
     * --------------------------------------------------------
     * break    立即跳出整个循环，散伙走人；
     * continue 跳过本轮剩下的，直接进下一轮。
     * 同样在 i==3 触发：break 输出 1 2；continue 输出 1 2 4 5。
     * ======================================================== */
    printf("【演示5-1】break 在 i==3 跳出：");
    for (int k = 1; k <= 5; k++) {
        if (k == 3) break;               /* 直接退出，3 4 5 都没了 */
        printf("%d ", k);
    }
    printf("\n【演示5-2】continue 在 i==3 跳过：");
    for (int k = 1; k <= 5; k++) {
        if (k == 3) continue;            /* 只跳过 3，循环继续 */
        printf("%d ", k);
    }
    printf("\n");

    /* ========================================================
     * 演示 6：循环嵌套 —— 直角三角形（对应讲义 七）
     * --------------------------------------------------------
     * 套路：外层控制"行数"，内层控制"每行打几个"。
     * ⚠️ 内外层必须用不同的循环变量（外 i、内 j）。
     * 内层条件 j<=i，所以第 i 行打 i 个星号，打出三角形。
     * 内层循环体总共执行 1+2+3+4 = 10 次。
     * ======================================================== */
    printf("【演示6】4 行直角三角形：\n");
    for (int row = 1; row <= 4; row++) { /* 外层：4 行 */
        printf("    ");
        for (int col = 1; col <= row; col++) {  /* 内层：第 row 行打 row 个 */
            printf("*");
        }
        printf("\n");                    /* 内层打完一行，换行 */
    }

    /* ========================================================
     * 演示 7：打擂台求最大值（对应讲义 九①）
     * --------------------------------------------------------
     * 套路：先拿第一个数当"擂主"，后面每个来比，更大就替换。
     * 求最小值只需把 > 改成 <。
     * ======================================================== */
    int data[5] = {3, 9, 2, 7, 5};       /* 数组后面模块才细讲，这里先借用 */
    int max = data[0];                   /* 第一个当擂主 */
    for (int t = 1; t < 5; t++) {
        if (data[t] > max) {             /* 来挑战的比擂主大 */
            max = data[t];               /* 换擂主 */
        }
    }
    printf("【演示7】{3,9,2,7,5} 的最大值 = %d\n", max);

    return 0;
}
