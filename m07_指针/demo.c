/* ============================================================
 * M7 指针 —— 配套演示程序（demo）
 * ------------------------------------------------------------
 * 指针是 C 语言最大的拦路虎，这个程序把"讲义"里的每个核心
 * 考点都用可运行的代码演示一遍。建议你：
 *   1) 先读讲义对应小节（多画箭头图），再回来看这里的演示；
 *   2) 编译运行，亲眼看到地址和值的变化，再对照注释里说的"为什么"。
 *
 * 编译运行（在本文件所在目录打开终端）：
 *   gcc demo.c -o demo
 *   ./demo
 *
 * 全用"固定值"演示，不需要你反复输入。练手输入请看 练习.c。
 * ============================================================ */

#include <stdio.h>

/* ============================================================
 * 函数声明 —— 演示 4、5 要用，先在 main 前面打招呼
 * ============================================================ */
void swap(int *a, int *b);                          /* 传地址，真交换 */
void divmod(int x, int y, int *q, int *r);          /* 用两个指针参数"返回"两个值 */

int main(void)
{
    /* ========================================================
     * 演示 1：& 取地址 + * 解引用 —— 全章最重要（对应讲义 二）
     * --------------------------------------------------------
     * 每个变量都有"值"和"地址"两样东西，是两码事。
     *   &n   取出 n 的地址（门牌号）；
     *   p    指针变量，里面装的是 n 的地址（不是 10！）；
     *   *p   顺着 p 里的地址找过去，拿到 n 的值。
     * %p 是打印地址的占位符（一串十六进制）。
     * ======================================================== */
    int n = 10;
    int *p = &n;                     /* p 指向 n：把 n 的地址装进 p */
    printf("【演示1】n 的值=%d，n 的地址=%p，*p=%d（顺着地址取到的值）\n",
           n, (void *)&n, *p);

    /* ========================================================
     * 演示 2：*p 不仅能读，还能写 —— 通过指针改变量（对应讲义 二）
     * --------------------------------------------------------
     * 给 *p 赋值，等价于直接改 n 本人。
     *   *p = 99;  完全等价于  n = 99;
     * 这是指针最大的威力，也是下面 swap 能成功的根本原因。
     * ======================================================== */
    *p = 99;                         /* 顺着地址把 n 房间里的值换成 99 */
    printf("【演示2】执行 *p=99 后，n=%d（n 真被改了，因为 *p 就是 n）\n", n);

    /* ========================================================
     * 演示 3：数组名是首元素地址，a[i] ⟺ *(a+i)（对应讲义 五）
     * --------------------------------------------------------
     * ⭐ 第8章最高频考点：下标访问本质就是指针运算。
     *   数组名 a 等价于 &a[0]（首元素地址）；
     *   a[2]、*(a+2)、q[2]、*(q+2) 四种写法访问同一个房间。
     * ⚠️ p+1 不是地址+1 字节，而是"跳过一整个元素"
     *    （int 占 4 字节，所以实际地址 +4）。
     * ======================================================== */
    int a[5] = {10, 20, 30, 40, 50};
    int *q = a;                      /* 数组名就是首地址，等价 q = &a[0] */
    printf("【演示3】a[2]=%d, *(a+2)=%d, q[2]=%d, *(q+2)=%d（四种写法同一个值）\n",
           a[2], *(a + 2), q[2], *(q + 2));

    /* ========================================================
     * 演示 4：用指针遍历数组求和（对应讲义 五、六）
     * --------------------------------------------------------
     * 指针是变量，p++ 让它指向下一个元素（数组名是常量，a++ 非法）。
     * 这里让 p 从首元素一路走到末尾，每步取 *p 累加。
     * ======================================================== */
    int sum = 0;
    for (int *pt = a; pt < a + 5; pt++) {  /* pt 从 a[0] 走到 a[4]，每次 +1 跳一个 int */
        sum = sum + *pt;             /* 取当前指向的元素累加 */
    }
    printf("【演示4】用指针遍历数组求和 = %d\n", sum);

    /* ========================================================
     * 演示 5：指针作函数参数 —— swap 真交换（对应讲义 七）
     * --------------------------------------------------------
     * ⚠️ 呼应 M4：普通传值改不动外面（函数拿到的是复印件）。
     * 传地址三件套：形参用指针、调用加 &、函数里用 *。
     * 这样函数顺着地址操作的就是 main 里的 x、y 本人。
     * ======================================================== */
    int x = 1, y = 2;
    printf("【演示5】交换前：x=%d, y=%d\n", x, y);
    swap(&x, &y);                    /* 传地址进去（加 &），不是传值 */
    printf("    交换后：x=%d, y=%d（成功！因为传的是地址）\n", x, y);

    /* ========================================================
     * 演示 6：用多个指针参数"返回"多个值（对应讲义 七）
     * --------------------------------------------------------
     * return 一次只能带回一个值。想同时拿到商和余数，
     * 就传两个指针参数，函数把结果写回指针指向的变量。
     * ======================================================== */
    int shang, yushu;
    divmod(17, 5, &shang, &yushu);   /* 把 shang、yushu 的地址传进去接结果 */
    printf("【演示6】17 除以 5：商=%d，余数=%d（指针带回两个结果）\n",
           shang, yushu);

    /* ========================================================
     * 演示 7：野指针的"安全"对照 —— 指针必须初始化（对应讲义 四）
     * --------------------------------------------------------
     * 这里不演示崩溃（野指针/解引用 NULL 会让程序当场挂掉），
     * 只演示正确姿势：定义即初始化，用前可检查是否为空。
     * ======================================================== */
    int *safe = NULL;                /* 暂时不指向任何东西，先置空更安全 */
    if (safe == NULL) {
        printf("【演示7】safe 现在是空指针（NULL），用前要检查，绝不能 *safe 解引用\n");
    }
    safe = &n;                       /* 指向确定的变量后才能安全使用 */
    printf("    让 safe 指向 n 后，*safe=%d\n", *safe);

    return 0;
}

/* ============================================================
 * 函数定义
 * ============================================================ */

/* 形参是指针，收到的是 main 里 x、y 的地址 */
void swap(int *a, int *b)
{
    int t = *a;                      /* 顺着地址取出 a 指向的值 */
    *a = *b;                         /* 把 b 指向的值写进 a 指向的房间 */
    *b = t;                          /* *a *b 操作的就是外面的 x y 本人 */
}

/* 商写回 q 指向的变量，余数写回 r 指向的变量 */
void divmod(int x, int y, int *q, int *r)
{
    *q = x / y;                      /* 商 */
    *r = x % y;                      /* 余数 */
}
